#!/usr/bin/env node
/**
 * Generate dist/docs/concepts.json — the machine-readable catalog of Lattice's
 * concept ontology: the nodes (the four axes Function · Form · Substance ·
 * Finish, the structural nouns Frame · Cell · Tile, the Component join, and the
 * Bucket grouping) and the typed relationships between them — the §2 lattice and
 * §7 edge table of design/concepts.md, encoded as data beside components.json
 * and forms.json. Also runs the **ontology drift gate** (via lib/concepts): every
 * node that claims a live catalog encodes it must resolve to a real, non-empty
 * vocabulary in the actual code, and the counts are derived from those catalogs —
 * so the relationship graph can't drift from what the engine really ships.
 *
 * Mirrors tools/build-forms.js (HARD RULE 15 — reuse the generator pattern): load
 * the ontology via the shared loader (lib/concepts), project it into one
 * deterministic document, and gate freshness with --check. The source of truth is
 * lib/concepts/concepts.json; this file is generated, so never hand-edit
 * dist/docs/concepts.json. Wired into tools/build.js after the forms catalog
 * (it cross-references both the component and the form catalogs).
 *
 * Usage:
 *   node tools/build-concepts.js            # regenerate dist/docs/concepts.json
 *   node tools/build-concepts.js --check    # verify it is fresh (CI gate); exit 1 if stale
 *
 * See design/concepts.md §9.
 */

const fs = require('node:fs');
const path = require('node:path');
const { loadConcepts } = require('../lib/concepts');

const ROOT = path.join(__dirname, '..');
const DOCS_DIR = path.join(ROOT, 'dist', 'docs');
const JSON_FILE = path.join(DOCS_DIR, 'concepts.json');

function renderJson() {
  // loadConcepts() runs the shape, integrity, and drift gates — it throws on any
  // failure, so a drifted ontology fails the build loud, before we touch the catalog.
  const { model, modelHref, counts, nodes, edges } = loadConcepts();
  const doc = {
    $comment:
      "Generated by tools/build-concepts.js from lib/concepts/concepts.json — do not edit by hand. The machine-readable catalog of Lattice's concept ontology: the four axes (Function · Form · Substance · Finish), the structural nouns (Frame · Cell · Tile), the Component join, the Bucket grouping, and the typed relationships between them. Counts are derived live from components.json / forms.json. See design/concepts.md.",
    model,
    modelHref,
    counts,
    nodes,
    edges,
  };
  return `${JSON.stringify(doc, null, 2)}\n`;
}

function isStale(file, content) {
  const current = fs.existsSync(file) ? fs.readFileSync(file, 'utf8') : null;
  return current !== content;
}

function main(argv) {
  const check = argv.includes('--check');
  const json = renderJson();
  fs.mkdirSync(DOCS_DIR, { recursive: true });
  const label = 'dist/docs/concepts.json';
  if (check) {
    if (isStale(JSON_FILE, json)) {
      process.stderr.write(`stale: ${label} — run \`node tools/build-concepts.js\` to regenerate.\n`);
      return 1;
    }
    process.stdout.write('concepts catalog up to date.\n');
    return 0;
  }
  if (isStale(JSON_FILE, json)) {
    fs.writeFileSync(JSON_FILE, json);
    process.stdout.write(`wrote ${label}\n`);
  } else {
    process.stdout.write('no changes (concepts catalog up to date).\n');
  }
  return 0;
}

if (require.main === module) {
  try {
    process.exit(main(process.argv.slice(2)));
  } catch (e) {
    // Clean gate failure (e.g. ontology drift) — message, not a raw stack.
    process.stderr.write(`${e.message}\n`);
    process.exit(1);
  }
}

module.exports = { renderJson, JSON_FILE };
