#!/usr/bin/env node
/**
 * CSS bundler. Concatenates lib/_*.css + lib/components/<name>/styles.css
 * into the shipped lattice.css.
 *
 * Source order (declared below):
 *   1. lib/_theme.css            Marp @theme directive + header comment
 *   2. lib/_base.css             resets
 *   3. lib/_root.css             :root tokens, font-face declarations
 *   4. lib/_scaffold.css         section, header, footer, pagination
 *   5. (@layer declaration emitted here)
 *   6. lib/components/<a..z>/styles.css   per-component, alphabetical
 *   7. lib/_legacy.css           residual non-extracted rules from old lattice.css
 *   8. lib/_semi-universal.css   compact, loose, accent
 *   9. lib/_universal.css        dark, with-period, bg-*, state, tone, chrome
 *  10. lib/_diagram-overrides.css  Mermaid theme overrides
 *
 * Missing source files are silently skipped — this lets the bundler
 * ramp up during the per-component migration (lib/components per-folder
 * styles.css files start empty; lib/_universal.css etc. land one at a time).
 *
 * Usage:
 *   node tools/build-css.js          # write lattice.css
 *   node tools/build-css.js --check  # exit 1 if stale (CI gate)
 *   npm run css:build / css:check
 *
 * See docs/design-system.md §10.
 */

const fs = require('node:fs');
const path = require('node:path');

const ROOT = path.join(__dirname, '..');
const OUTPUT = path.join(ROOT, 'lattice.css');

const HEAD_SOURCES = ['lib/_theme.css', 'lib/_base.css', 'lib/_root.css', 'lib/_scaffold.css'];
const TAIL_SOURCES = [
  'lib/_legacy.css',
  'lib/_semi-universal.css',
  'lib/_universal.css',
  'lib/_diagram-overrides.css',
];

const LAYER_DECLARATION =
  '@layer base, root, scaffold, components, semi-universal, universal, diagram-overrides;';

const BANNER = [
  '/* lattice.css — GENERATED by tools/build-css.js.',
  ' * Do not edit by hand. Re-run `npm run css:build` after touching',
  ' * any source file in lib/_*.css or lib/components/<name>/styles.css.',
  ' * CI gate: `npm run css:check`.',
  ' */',
  '',
].join('\n');

function readIfExists(rel) {
  const abs = path.join(ROOT, rel);
  if (!fs.existsSync(abs)) return null;
  return fs.readFileSync(abs, 'utf8').replace(/\s+$/, '') + '\n';
}

function componentStyles() {
  const dir = path.join(ROOT, 'lib', 'components');
  if (!fs.existsSync(dir)) return [];
  const names = fs
    .readdirSync(dir, { withFileTypes: true })
    .filter((e) => e.isDirectory())
    .map((e) => e.name)
    .sort();
  const out = [];
  for (const name of names) {
    const rel = path.join('lib', 'components', name, 'styles.css');
    const text = readIfExists(rel);
    if (text) out.push({ rel, text });
  }
  return out;
}

function bundle() {
  const parts = [BANNER];
  // Marp @theme must come before any @layer or other at-rule that
  // affects parsing, so HEAD sources come first.
  for (const rel of HEAD_SOURCES) {
    const text = readIfExists(rel);
    if (text) {
      parts.push(`/* === ${rel} === */`);
      parts.push(text);
    }
  }
  // Declare the cascade once for the whole bundle.
  parts.push(LAYER_DECLARATION);
  parts.push('');
  // Per-component CSS, alphabetical (within @layer components, source
  // order doesn't matter — selectors are scoped to section.<name>).
  for (const { rel, text } of componentStyles()) {
    parts.push(`/* === ${rel} === */`);
    parts.push(text);
  }
  // Tail: legacy residual + semi-universal + universal + diagram overrides.
  for (const rel of TAIL_SOURCES) {
    const text = readIfExists(rel);
    if (text) {
      parts.push(`/* === ${rel} === */`);
      parts.push(text);
    }
  }
  return parts.join('\n');
}

function main(argv) {
  const fresh = bundle();
  if (argv.includes('--check')) {
    const current = fs.existsSync(OUTPUT) ? fs.readFileSync(OUTPUT, 'utf8') : '';
    if (current === fresh) {
      process.stdout.write('lattice.css is up to date.\n');
      return 0;
    }
    process.stderr.write('error: lattice.css is stale relative to sources.\n');
    process.stderr.write('       Run `npm run css:build` to regenerate.\n');
    return 1;
  }
  fs.writeFileSync(OUTPUT, fresh);
  process.stdout.write(`wrote lattice.css (${fresh.length.toLocaleString()} bytes)\n`);
  return 0;
}

if (require.main === module) process.exit(main(process.argv.slice(2)));

module.exports = { bundle, main, OUTPUT };
