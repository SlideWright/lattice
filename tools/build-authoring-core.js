#!/usr/bin/env node
/**
 * Bundle the pure authoring engines for the browser.
 *
 *   lib/authoring/{lint-core,review-core,scorecard,notes-core}.js
 *     →  docs/src/playground/authoring-core.generated.js   (ESM, named exports)
 *
 * WHY a bundle: the Drawing Board's Architect / Coach panels run the SAME
 * lint / review / scorecard checks as the Node CLI, so they import the repo's
 * pure CommonJS authoring cores. A *direct* `import x from '../../../lib/...'`
 * works in the Rollup BUILD (astro.config's commonjsOptions converts it) but
 * NOT in `astro dev`: Vite serves a source CJS file fetched over /@fs without
 * the CJS→ESM transform, so its `module.exports` reads as no `default` export
 * and the whole Architect <script> block fails to load (the panels go dead in
 * dev). esbuild bundling emits one in-root ESM module with real named exports
 * that Vite serves in dev AND Rollup ships in the build — the same fix the
 * Theme Studio (theme-core.generated.js) and Layout Studio
 * (layout-core.generated.js) already use.
 *
 * There is exactly one source (lib/authoring/*) — the Node tooling, the
 * manifest `validate()` gate, and the browser all run it; this is only a
 * packaging step. Each core is dependency-free, so the bundle is its four
 * modules and nothing else. (notes-core is HARD RULE #1's single note/non-note
 * boundary — the Practice read-aloud surfaces notes through this same bundle.)
 *
 * Flags:
 *   --check    Build to a temp buffer and diff against the committed file.
 *              Exits 1 on drift (the freshness gate).
 *   --silent   Suppress the success log line.
 */

const esbuild = require('esbuild');
const path = require('path');
const fs = require('fs');

const ROOT = path.resolve(__dirname, '..');
const SRC_DIR = path.join(ROOT, 'lib', 'authoring');
const OUT_FILE = path.join(ROOT, 'docs', 'src', 'playground', 'authoring-core.generated.js');

const argv = process.argv.slice(2);
const check = argv.includes('--check');
const silent = argv.includes('--silent') || check;

// Default-import each CJS module (esbuild's bundle-time interop binds the
// default to module.exports) and re-export each as a named object, so call
// sites keep the namespace style they use today (lintCore.applyFix, …).
const ENTRY_CONTENTS = `
import lintCore from './lint-core.js';
import reviewCore from './review-core.js';
import scorecard from './scorecard.js';
import notesCore from './notes-core.js';

export { lintCore, reviewCore, scorecard, notesCore };
`;

const BUILD_OPTIONS = {
  stdin: { contents: ENTRY_CONTENTS, resolveDir: SRC_DIR, loader: 'js', sourcefile: 'authoring-core.entry.js' },
  bundle: true,
  format: 'esm',
  platform: 'browser',
  target: ['chrome109'],
  minify: false, // readable: this is committed source the docs build serves
  legalComments: 'none',
  banner: {
    js: '/* Auto-generated by tools/build-authoring-core.js — DO NOT EDIT.\n   Source: lib/authoring/{lint-core,review-core,scorecard,notes-core}.js. Rebuild: npm run authoring-core:build */',
  },
};

async function build() {
  const result = await esbuild.build({ ...BUILD_OPTIONS, write: false });
  return result.outputFiles[0].text;
}

async function main() {
  const out = await build();
  if (check) {
    const current = fs.existsSync(OUT_FILE) ? fs.readFileSync(OUT_FILE, 'utf8') : '';
    if (current !== out) {
      console.error('[build-authoring-core] STALE — run `npm run authoring-core:build` and commit docs/src/playground/authoring-core.generated.js');
      process.exit(1);
    }
    if (!silent) console.log('[build-authoring-core] up to date.');
    return;
  }
  fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
  fs.writeFileSync(OUT_FILE, out);
  if (!silent) console.log(`[build-authoring-core] wrote ${path.relative(ROOT, OUT_FILE)} (${out.length} bytes)`);
}

main().catch(e => {
  console.error('[build-authoring-core] failed:', e.message);
  process.exit(1);
});
