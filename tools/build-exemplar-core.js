#!/usr/bin/env node
/**
 * Bundle the pure exemplar tier-filter for the browser (sibling of
 * tools/build-authoring-core.js).
 *
 *   lib/exemplars/tier-filter.js
 *     →  docs/src/playground/exemplar-core.generated.js   (ESM, named exports)
 *
 * WHY a bundle: the Drawing Board's Drafting picker trims a worked exemplar deck
 * to the requested length (short / standard / full) with the SAME pure filter the
 * CLI and the unit suite run (lib/exemplars/tier-filter.js). A *direct* import of
 * the source CommonJS file works in the Rollup build but NOT in `astro dev` (Vite
 * serves the raw CJS over /@fs with no CJS→ESM transform, so `module.exports`
 * reads as no `default` export and the picker's worked-example path dies in dev).
 * esbuild emits one in-root ESM module with real named exports that Vite serves in
 * dev AND Rollup ships in the build — the same fix the authoring / layout / theme
 * cores already use.
 *
 * There is exactly ONE source (lib/exemplars/tier-filter.js) — the CLI, the docs
 * build, and the browser all run it; this is only a packaging step.
 *
 * Flags: --check (freshness gate, exit 1 on drift), --silent.
 */

const esbuild = require('esbuild');
const path = require('path');
const fs = require('fs');

const ROOT = path.resolve(__dirname, '..');
const SRC_DIR = path.join(ROOT, 'lib', 'exemplars');
const OUT_FILE = path.join(ROOT, 'docs', 'src', 'playground', 'exemplar-core.generated.js');

const argv = process.argv.slice(2);
const check = argv.includes('--check');
const silent = argv.includes('--silent') || check;

// Default-import the CJS module (esbuild binds the default to module.exports) and
// re-export each member as a named export, so call sites read filterToTier /
// tierCounts directly.
const ENTRY_CONTENTS = `
import tierFilter from './tier-filter.js';

export const { filterToTier, tierCounts, splitDeck, slideTier, TIERS, DEFAULT_TIER } = tierFilter;
`;

const BUILD_OPTIONS = {
  stdin: { contents: ENTRY_CONTENTS, resolveDir: SRC_DIR, loader: 'js', sourcefile: 'exemplar-core.entry.js' },
  bundle: true,
  format: 'esm',
  platform: 'browser',
  target: ['chrome109'],
  minify: false, // readable: this is committed source the docs build serves
  legalComments: 'none',
  banner: {
    js: '/* Auto-generated by tools/build-exemplar-core.js — DO NOT EDIT.\n   Source: lib/exemplars/tier-filter.js. Rebuild: npm run exemplar-core:build */',
  },
};

async function build() {
  const result = await esbuild.build({ ...BUILD_OPTIONS, write: false });
  return result.outputFiles[0].text;
}

async function main() {
  const out = await build();
  if (check) {
    const current = fs.existsSync(OUT_FILE) ? fs.readFileSync(OUT_FILE, 'utf8') : '';
    if (current !== out) {
      console.error('[build-exemplar-core] STALE — run `npm run exemplar-core:build` and commit docs/src/playground/exemplar-core.generated.js');
      process.exit(1);
    }
    if (!silent) console.log('[build-exemplar-core] up to date.');
    return;
  }
  fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
  fs.writeFileSync(OUT_FILE, out);
  if (!silent) console.log(`[build-exemplar-core] wrote ${path.relative(ROOT, OUT_FILE)} (${out.length} bytes)`);
}

main().catch(e => {
  console.error('[build-exemplar-core] failed:', e.message);
  process.exit(1);
});
