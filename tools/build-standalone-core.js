#!/usr/bin/env node
/**
 * Bundle the standalone chart-SVG export core for the browser.
 *
 *   lib/components/chart/_chart-family/standalone-svg.js
 *     →  docs/src/playground/standalone-svg.generated.js   (ESM, named exports)
 *
 * Same rationale as the theme/layout/authoring cores (tools/build-*-core.js):
 * the lib module is CommonJS (so the CLI tools/export-chart-svg.js and the Node
 * unit tests share it), which Vite's dev server can't transform in an arbitrary
 * source file — `module is not defined`. esbuild resolves it into one ESM module
 * with real named exports for docs/src/playground/drawing-board-export.js's
 * "download chart as SVG" action. ONE source (the same the unit tests run); this
 * is only a packaging step. The output lives under docs/ as a docs-site asset.
 *
 * Flags: --check (freshness gate, exit 1 on drift), --silent.
 */

const esbuild = require('esbuild');
const path = require('path');
const fs = require('fs');

const ROOT = path.resolve(__dirname, '..');
const SRC_DIR = path.join(ROOT, 'lib', 'components', 'chart', '_chart-family');
const OUT_FILE = path.join(ROOT, 'docs', 'src', 'playground', 'standalone-svg.generated.js');

const argv = process.argv.slice(2);
const check = argv.includes('--check');
const silent = argv.includes('--silent') || check;

// Re-export the CJS module's members as ESM named exports (mirrors the other
// *-core entries). flattenSvgStyles stays closure-free so the CLI can also
// .toString() it into a puppeteer page.
const ENTRY_CONTENTS = `
import core from './standalone-svg.js';
export const {
  STYLE_PROPS, INITIAL, SVG_NS,
  flattenSvgStyles, collectFontFamilies, finalizeStandaloneSvg,
} = core;
`;

const BUILD_OPTIONS = {
  stdin: { contents: ENTRY_CONTENTS, resolveDir: SRC_DIR, loader: 'js', sourcefile: 'standalone-core.entry.js' },
  bundle: true,
  format: 'esm',
  platform: 'browser',
  target: ['chrome109'],
  minify: false,
  legalComments: 'none',
  banner: {
    js: '/* Auto-generated by tools/build-standalone-core.js — DO NOT EDIT.\n   Source: lib/components/chart/_chart-family/standalone-svg.js. Rebuild: npm run standalone-core:build */',
  },
};

async function build() {
  const result = await esbuild.build({ ...BUILD_OPTIONS, write: false });
  return result.outputFiles[0].text;
}

async function main() {
  const out = await build();
  if (check) {
    const current = fs.existsSync(OUT_FILE) ? fs.readFileSync(OUT_FILE, 'utf8') : '';
    if (current !== out) {
      console.error('[build-standalone-core] STALE — run `npm run standalone-core:build` and commit docs/src/playground/standalone-svg.generated.js');
      process.exit(1);
    }
    if (!silent) console.log('[build-standalone-core] up to date.');
    return;
  }
  fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
  fs.writeFileSync(OUT_FILE, out);
  if (!silent) console.log(`[build-standalone-core] wrote ${path.relative(ROOT, OUT_FILE)} (${out.length} bytes)`);
}

main().catch(e => {
  console.error('[build-standalone-core] failed:', e.message);
  process.exit(1);
});
