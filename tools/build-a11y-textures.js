#!/usr/bin/env node
/**
 * Bundle the categorical/chart texture-<defs> kernel for the browser.
 *
 *   lib/core/accessibility-textures.js
 *     →  docs/src/playground/a11y-textures.generated.js   (ESM, named export)
 *
 * WHY a bundle: deck-preview.js (the one shared filmstrip-preview controller)
 * must inject the a11y texture <defs> on every render so every surface — Drawing
 * Board, Playground, both Workbench studios, Present, Practice — shows a11y
 * textures identically. It can't `import` the CJS kernel directly (Vite/Rollup
 * don't transform `module.exports` from arbitrary out-of-root files — it errors
 * at bundle time). esbuild resolves the CJS and emits one ESM module with a real
 * named export. There is exactly ONE source of the pattern geometry
 * (lib/core/accessibility-textures.js, shared with the emulator); this is only a
 * packaging step. Mirrors tools/build-theme-core.js.
 *
 * Flags:
 *   --check    Build to a buffer and diff against the committed file (freshness
 *              gate); exits 1 on drift.
 *   --silent   Suppress the success log line.
 */

const esbuild = require('esbuild');
const path = require('path');
const fs = require('fs');

const ROOT = path.resolve(__dirname, '..');
const SRC_DIR = path.join(ROOT, 'lib', 'core');
const OUT_FILE = path.join(ROOT, 'docs', 'src', 'playground', 'a11y-textures.generated.js');

const argv = process.argv.slice(2);
const check = argv.includes('--check');
const silent = argv.includes('--silent') || check;

// Default-import the CJS module (esbuild binds the default to module.exports),
// then re-export the public symbol as a named ESM export.
const ENTRY_CONTENTS = `
import mod from './accessibility-textures.js';
export const { texturePatternDefs } = mod;
`;

const BUILD_OPTIONS = {
  stdin: { contents: ENTRY_CONTENTS, resolveDir: SRC_DIR, loader: 'js', sourcefile: 'a11y-textures.entry.js' },
  bundle: true,
  format: 'esm',
  platform: 'browser',
  target: ['chrome109'],
  minify: false, // readable: committed source the docs build serves
  legalComments: 'none',
  banner: {
    js: '/* Auto-generated by tools/build-a11y-textures.js — DO NOT EDIT.\n   Source: lib/core/accessibility-textures.js. Rebuild: npm run a11y-textures:build */',
  },
};

async function build() {
  const result = await esbuild.build({ ...BUILD_OPTIONS, write: false });
  return result.outputFiles[0].text;
}

async function main() {
  const out = await build();
  if (check) {
    const current = fs.existsSync(OUT_FILE) ? fs.readFileSync(OUT_FILE, 'utf8') : '';
    if (current !== out) {
      console.error('[build-a11y-textures] STALE — run `npm run a11y-textures:build` and commit docs/src/playground/a11y-textures.generated.js');
      process.exit(1);
    }
    if (!silent) console.log('[build-a11y-textures] up to date.');
    return;
  }
  fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
  fs.writeFileSync(OUT_FILE, out);
  if (!silent) console.log(`[build-a11y-textures] wrote ${path.relative(ROOT, OUT_FILE)} (${out.length} bytes)`);
}

main().catch((e) => {
  console.error('[build-a11y-textures] failed:', e.message);
  process.exit(1);
});
