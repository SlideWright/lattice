#!/usr/bin/env node
/**
 * Bundle the pure Theme Studio core for the browser.
 *
 *   lib/theme/{color,derive,contrast,serialize,starters}.js
 *     →  docs/src/playground/theme-core.generated.js   (ESM, named exports)
 *
 * WHY a bundle (not a direct import like the Architect does with lint-core):
 * lib/theme's modules `require()` each other, and Vite's DEV server does not
 * transform CJS `require` in arbitrary source files (it works only for
 * dependency-free leaf modules). esbuild bundling resolves the whole CJS graph
 * and emits one ESM module with real named exports that Vite serves in dev AND
 * Rollup ships in the build. This mirrors tools/build-playground.js (the engine
 * bundle); the output lives under docs/ because it's a docs-site asset, not
 * part of the published npm engine package.
 *
 * The browser uses the SAME derivation + contrast maths as the Node tooling and
 * the WCAG gate — there is exactly one source (lib/theme/*); this is only a
 * packaging step.
 *
 * Flags:
 *   --check    Build to a temp buffer and diff against the committed file.
 *              Exits 1 on drift (the freshness gate).
 *   --silent   Suppress the success log line.
 */

const esbuild = require('esbuild');
const path = require('path');
const fs = require('fs');

const ROOT = path.resolve(__dirname, '..');
const SRC_DIR = path.join(ROOT, 'lib', 'theme');
const OUT_FILE = path.join(ROOT, 'docs', 'src', 'playground', 'theme-core.generated.js');

const argv = process.argv.slice(2);
const check = argv.includes('--check');
const silent = argv.includes('--silent') || check;

// Default-import each CJS module (esbuild's bundle-time interop binds the
// default to module.exports) and explicitly re-export the public symbols as
// named ESM exports. `export *` doesn't work here — CJS exports aren't
// statically enumerable, so esbuild can't surface them by name.
const ENTRY_CONTENTS = `
import color from './color.js';
import derive from './derive.js';
import contrast from './contrast.js';
import serialize from './serialize.js';
import starters from './starters.js';
import ai from './ai.js';

// color
export const {
  normalizeHex, hexToRgb, rgbToHex, relativeLuminance, contrastRatio,
  AA, AA_LARGE, AAA, hexToOklch, oklchToHex, withLightness, withChroma,
  rotateHue, mix, pickInk, ensureContrast,
} = color;
// derive
export const {
  ESSENTIAL_KEYS, REQUIRED_TOKENS, requiredTokenList, validateEssentials,
  deriveTheme, PALE_L, DEEP_L,
} = derive;
// contrast
export const { resolveVars, contractPairs, contentPairs, auditVars, auditBoth, meter } = contrast;
// serialize
export const { serializeTheme, themeAsset } = serialize;
// starters
export const { STARTERS, getStarter } = starters;
// ai (Phase 2 — one prompt builder + reply coercion; the model call is the caller's)
export const { ASK_SYSTEM, askMessages, coerceEssentials } = ai;
`;

const BUILD_OPTIONS = {
  stdin: { contents: ENTRY_CONTENTS, resolveDir: SRC_DIR, loader: 'js', sourcefile: 'theme-core.entry.js' },
  bundle: true,
  format: 'esm',
  platform: 'browser',
  target: ['chrome109'],
  minify: false, // readable: this is committed source the docs build serves
  legalComments: 'none',
  banner: {
    js: '/* Auto-generated by tools/build-theme-core.js — DO NOT EDIT.\n   Source: lib/theme/*.js. Rebuild: npm run theme-core:build */',
  },
};

async function build() {
  const result = await esbuild.build({ ...BUILD_OPTIONS, write: false });
  return result.outputFiles[0].text;
}

async function main() {
  const out = await build();
  if (check) {
    const current = fs.existsSync(OUT_FILE) ? fs.readFileSync(OUT_FILE, 'utf8') : '';
    if (current !== out) {
      console.error('[build-theme-core] STALE — run `npm run theme-core:build` and commit docs/src/playground/theme-core.generated.js');
      process.exit(1);
    }
    if (!silent) console.log('[build-theme-core] up to date.');
    return;
  }
  fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
  fs.writeFileSync(OUT_FILE, out);
  if (!silent) console.log(`[build-theme-core] wrote ${path.relative(ROOT, OUT_FILE)} (${out.length} bytes)`);
}

main().catch(e => {
  console.error('[build-theme-core] failed:', e.message);
  process.exit(1);
});
