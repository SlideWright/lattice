#!/usr/bin/env node
/**
 * Bundle the pure Layout Studio core for the browser (the Form-layer sibling of
 * tools/build-theme-core.js).
 *
 *   lib/layout/{gate,scaffold,starters}.js
 *     →  docs/src/playground/layout-core.generated.js   (ESM, named exports)
 *
 * Same rationale as the theme core: lib/layout's modules `require()` each other,
 * which Vite's dev server can't transform in arbitrary source files, so esbuild
 * resolves the CJS graph into one ESM module with real named exports. There is
 * exactly ONE source (lib/layout/*) — the SAME gates the Node unit tests run;
 * this is only a packaging step. The output lives under docs/ because it's a
 * docs-site asset, not part of the published npm engine package.
 *
 * Flags: --check (freshness gate, exit 1 on drift), --silent.
 */

const esbuild = require('esbuild');
const path = require('path');
const fs = require('fs');

const ROOT = path.resolve(__dirname, '..');
const SRC_DIR = path.join(ROOT, 'lib', 'layout');
const OUT_FILE = path.join(ROOT, 'docs', 'src', 'playground', 'layout-core.generated.js');

const argv = process.argv.slice(2);
const check = argv.includes('--check');
const silent = argv.includes('--silent') || check;

const ENTRY_CONTENTS = `
import gate from './gate.js';
import scaffold from './scaffold.js';
import starters from './starters.js';

// gate (deterministic — model proposes, these dispose)
export const {
  FUNCTIONS, BUCKETS, FORMS, SUBSTANCES, CSS_ONLY_SUBSTANCES, NAME_RE,
  findHexLiterals, findUnscopedSelectors, validateManifest,
  skeletonInvokes, gateCss, gateComponent,
} = gate;
// scaffold (clean draft → graduation files + asset record)
export const {
  MANIFEST_ORDER, manifestObject, manifestJson, stylesCss, skeletonMd,
  scaffoldFiles, scaffoldDir, componentAsset,
} = scaffold;
// starters (the no-model floor + the head start)
export const { STARTERS, getStarter } = starters;
`;

const BUILD_OPTIONS = {
  stdin: { contents: ENTRY_CONTENTS, resolveDir: SRC_DIR, loader: 'js', sourcefile: 'layout-core.entry.js' },
  bundle: true,
  format: 'esm',
  platform: 'browser',
  target: ['chrome109'],
  minify: false,
  legalComments: 'none',
  banner: {
    js: '/* Auto-generated by tools/build-layout-core.js — DO NOT EDIT.\n   Source: lib/layout/*.js. Rebuild: npm run layout-core:build */',
  },
};

async function build() {
  const result = await esbuild.build({ ...BUILD_OPTIONS, write: false });
  return result.outputFiles[0].text;
}

async function main() {
  const out = await build();
  if (check) {
    const current = fs.existsSync(OUT_FILE) ? fs.readFileSync(OUT_FILE, 'utf8') : '';
    if (current !== out) {
      console.error('[build-layout-core] STALE — run `npm run layout-core:build` and commit docs/src/playground/layout-core.generated.js');
      process.exit(1);
    }
    if (!silent) console.log('[build-layout-core] up to date.');
    return;
  }
  fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
  fs.writeFileSync(OUT_FILE, out);
  if (!silent) console.log(`[build-layout-core] wrote ${path.relative(ROOT, OUT_FILE)} (${out.length} bytes)`);
}

main().catch(e => {
  console.error('[build-layout-core] failed:', e.message);
  process.exit(1);
});
