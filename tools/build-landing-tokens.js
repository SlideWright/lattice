#!/usr/bin/env node
/**
 * Emit the per-palette / per-mode CSS token blocks the docs LANDING page
 * consumes, derived from the exact same resolver the component portal uses
 * (tools/build-docs-portal.js → paletteCss). This is the parity guarantee:
 * the landing page and the component portal both resolve their colour tokens
 * from themes/<name>.css, so a visitor's palette/mode choice looks identical
 * on both and persists across the jump (shared localStorage keys).
 *
 * Output: docs/src/styles/lattice-tokens.generated.css — imported by
 * docs/src/styles/landing.css. Only the `html[data-palette][data-mode]`
 * token blocks live here; structural styling is hand-authored in landing.css.
 *
 * Usage:
 *   node tools/build-landing-tokens.js            # write the file
 *   node tools/build-landing-tokens.js --check    # CI gate (stale = exit 1)
 *
 * Exit codes:
 *   0  success (or --check: up to date)
 *   1  --check: the output is stale relative to the manifests/themes
 */

const fs = require('node:fs');
const path = require('node:path');
const { paletteCss, listBasePalettes } = require('./build-docs-portal');

const ROOT = path.join(__dirname, '..');
const OUT_FILE = path.join(ROOT, 'docs', 'src', 'styles', 'lattice-tokens.generated.css');

function render() {
  return `/* GENERATED by tools/build-landing-tokens.js — do not edit by hand.
 * Per-palette / per-mode colour tokens, resolved from themes/<name>.css via
 * the same paletteCss() the component portal uses. Run \`npm run docs:landing-tokens\`
 * to regenerate. Structural styling lives in landing.css. */

${paletteCss()}
`;
}

function isStale(file, content) {
  const current = fs.existsSync(file) ? fs.readFileSync(file, 'utf8') : null;
  return current !== content;
}

function main(argv) {
  const check = argv.includes('--check');
  const content = render();
  const label = 'docs/src/styles/lattice-tokens.generated.css';

  if (check) {
    if (isStale(OUT_FILE, content)) {
      process.stderr.write(`stale: ${label} — run \`node tools/build-landing-tokens.js\` to regenerate.\n`);
      return 1;
    }
    process.stdout.write('landing tokens up to date.\n');
    return 0;
  }

  if (isStale(OUT_FILE, content)) {
    fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
    fs.writeFileSync(OUT_FILE, content);
    process.stdout.write(`wrote ${label} (${listBasePalettes().length} palettes).\n`);
  } else {
    process.stdout.write('no changes (landing tokens up to date).\n');
  }
  return 0;
}

if (require.main === module) process.exit(main(process.argv.slice(2)));

module.exports = { render, OUT_FILE };
